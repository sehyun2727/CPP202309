#include <iostream>
#include <string>
using namespace std;

int main() {
	char character = 'C'; //문자형 변수 저장
	int integer = 100; // 정수형수 저장
	double precision = 3.141592; // 실수형 변수 저장
	bool is_true = true; //bool형 변수 저장
	string word = "Hello World"; //문자열 저장

	cout << "char :" << character << endl; //해당 문자열과 변수 출력
	cout << "int :" << integer << endl;
	cout << "double :" << precision << endl;
	cout << "bool :" << is_true << endl;
	cout << "string :" << word << endl;
}